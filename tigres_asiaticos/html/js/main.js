    loadContent: function (country, type) {
        var $content = $('#content'),
            $card = $('#ccard'),
            $image = $('#btnCCMImage'),
            $legend = $('#ccmlegend'),
            $ctext = $('#cctext');

        $content.find('.btn:not(.btn_close).disabled').removeClass('disabled');
        $card.removeClass('text media');
        $image
            .removeData('video')
            .attr('src', '')
            .removeClass('image video');
        $legend.html('');
        $ctext.html('');

        if (!(country in this))
            return;

        if (!(type in this[country]))
            return;

        $content.find('.btn[data-type="' + type + '"]').addClass('disabled');

        var data = this[country][type];

        if (data.type == 'text') {
            $card.addClass('text');

            if ('text' in data)
                $ctext.html(data.text);
        }
        else if (data.type == 'image' || data.type == 'video') {
            $card.addClass('media');

            $image.addClass(data.type);

            if ('video' in data)
                $image.data('video', data.video);

            if ('image' in data)
                $image.attr('src', data.image)

            if ('legend' in data)
                $legend.html(data.legend);

            if ('text' in data)
                $ctext.html(data.text);
        }
    },

